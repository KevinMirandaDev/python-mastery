# Arithmetic operators

## Addition
my_addition = 3 + 2   #Result: 5
print(my_addition)

## Subtraction
my_subtraction = 5 - 3 #Result: 2
print(my_subtraction)

## Multiplication
my_multiplication = 3 * 2  #Result: 6
print(my_multiplication)

## Division
my_division = 6/2 #Result: 3.0
print(my_division)

## Floor Division
my_floordivision = 10//3 #Result: 3
print(my_floordivision)

## Modulo
my_modulo = 10%3 #Result: 1
print(my_modulo)

## Power
my_power = 8**2 # 8^2. Result: 64
print(my_power)